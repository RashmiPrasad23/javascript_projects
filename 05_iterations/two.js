//while loop
let index=0;
while(index<=10){
  console.log(`value is ${index}`);
  index=index+2
}

let myArr=["flash","batman","superman"];
let arr=0;
while(arr<myArr.length){
    console.log(`value is ${myArr[arr]}`);
    arr=arr+1
}


//do while loop
let score=1;
do{
    console.log(`value is ${score}`);
    score++
}while(score<=10)